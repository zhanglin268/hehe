#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>
#include "game.h"
void menu()
{
	printf("*************************\n");
	printf("********  1.play  *******\n");
	printf("********  2.exit  *******\n");
	printf("*************************\n");
}
void game()
{
//存放布置好的的雷的信息
	char mine[ROWS][COLS] = { 0 };
	//存放排查出的雷的信息
	char show[ROWS][COLS] = { 0 };
	InitBoard(mine, ROWS, COLS,'0');
	InitBoard(show, ROWS, COLS,'*');
	//打印棋盘
	DisplayBoard(mine,ROW,COL);
	DisplayBoard(show, ROW, COL);

}
void test()
{
	int input = 0;
	do
	{

		menu();
		printf("请选择：》");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误");
			break;
		}
	} while (input);
}
int main()
{
	test();
	
	return 0;
}